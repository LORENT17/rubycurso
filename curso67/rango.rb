
=begin
.include?(value) => devuelve true or false
.last => devuelve el último objeto en el Range
.max => devuelve el valor máximo en el Range
.min => devuelve el valor mínimo en el Range
=end


puts (1..25).include?(28) #=> devuelve true or false
puts (2..25).min #=> devuelve el valor mínimo en el Range
puts (1..8).last #=> devuelve el último objeto en el Range
puts (1..16).max #=> devuelve el valor máximo en el Range
